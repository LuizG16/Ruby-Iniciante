count = 1 

loop do 
puts "Luiz"
puts count #mostra os numeros #deixa junto com puts's
if count == 10   
    break # Para o loop quando contador for maior que 5
end #fim
count += 1
end
# O if é uma estrutura condicional usada para executar um bloco de código apenas se uma determinada condição for verdadeira. Em Ruby, o if é muito simples.

#O break é usado em loops (como while, until, for ou .times). Ele serve para interromper o loop antes que ele termine de forma natural, dependendo de uma condição que você define. Quando o break é executado, o loop para imediatamente, e o código continua após o loop.